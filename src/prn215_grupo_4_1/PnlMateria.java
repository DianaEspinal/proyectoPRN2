    private void tbMateriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMateriaMouseClicked
        
        btnActualizar.setEnabled(true);
        btnEliminar.setEnabled(true);
        
        PreparedStatement ps = null;
        ResultSet rs = null;
        
        try {
            Conexion con = new Conexion();
            Connection conn = con.conectar();
            
            int fila = tbMateria.getSelectedRow();
            String codigoMateria = tbMateria.getValueAt(fila, 0).toString();
            
            ps = conn.prepareStatement("SELECT * FROM materia WHERE idMateria = ?");
            ps.setString(1, codigoMateria);
            rs = ps.executeQuery();
            
            while (rs.next()) 
            {
                txtMateria.setText(rs.getString("nombreMateria"));
                
            }
        } 
        catch (Exception e) 
        {
              JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_tbMateriaMouseClicked

 
