    /* Creacion de las diferentes funciones para mandar los datos capturados de los textboxes al controlador y ser mandados a las funciones SQL */
    void mostrarMaterias()
    {
               
        try 
        {
            DefaultTableModel modelo = new DefaultTableModel();
            tbMateria.setModel(modelo);
            
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.conectar();
            
            String sql = "SELECT * FROM materia;"; 
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            
            modelo.addColumn("Codigo Materia");
            modelo.addColumn("Nombre de la materia");
            while (rs.next()) 
            {
                Object[] filas = new Object[cantidadColumnas];
                
                for (int i = 0; i < cantidadColumnas; i++) 
                {
                    filas[i] = rs.getObject(i + 1);
                }
                
                modelo.addRow(filas);
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e);
        }       
        
        tbMateria.getColumnModel().getColumn(0).setMinWidth(0);
        tbMateria.getColumnModel().getColumn(0).setMaxWidth(0);
    }
    
    private void agregarMateria()
    {
        
        if (txtMateria.getText().isEmpty()) 
        {
            
            JOptionPane.showMessageDialog(this, "No dejar campos vacios", "Advertencia",JOptionPane.WARNING_MESSAGE);
            
        }
        else
        {
            
            controlador_materia.setNombreMateria(txtMateria.getText());
            funciones.agregarMateria(controlador_materia);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
            
        }
    }
    
    private void actualizarMateria()
    {
        if (txtMateria.getText().isEmpty()) 
        {
            
            JOptionPane.showMessageDialog(this, "No dejar campos vacios", "Advertencia",JOptionPane.WARNING_MESSAGE);
            
        }
        else
        {
            int fila = tbMateria.getSelectedRow();
            int idMateria = Integer.parseInt(tbMateria.getValueAt(fila, 0).toString());
            controlador_materia.setCodigoMateria(idMateria);
            controlador_materia.setNombreMateria(txtMateria.getText());
            funciones.actualizarMateria(controlador_materia);
            
        }
    }
    
    private void eliminarMateria()
    {
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Estás seguro que quieres eliminar esta materia?", "Eliminar registro", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) 
        {
            int fila = tbMateria.getSelectedRow();
            int idMateria = Integer.parseInt(tbMateria.getValueAt(fila, 0).toString());
            controlador_materia.setCodigoMateria(idMateria);
            funciones.eliminarMateria(controlador_materia);

        }
    } 
    
